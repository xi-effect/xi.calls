#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import { parseArgs } from 'node:util';
import { pathToFileURL } from 'node:url';
import { getRepoRoot, listCallsPackageNames } from './calls-publish-lib.mjs';

export function generatePackageFilters({ rootDir, outputFile }) {
  const packagesDir = path.join(rootDir, 'packages');
  const packageNames = listCallsPackageNames(packagesDir);

  const lines = ['# autogenerated packages data'];

  for (const name of packageNames) {
    const key = name.replaceAll('/', '-');
    lines.push(`${key}:`, `  - packages/${name}/**`);
  }

  fs.mkdirSync(path.dirname(outputFile), { recursive: true });
  fs.writeFileSync(outputFile, `${lines.join('\n')}\n`, 'utf8');

  return outputFile;
}

function parseCliArgs(argv) {
  const { values } = parseArgs({
    args: argv,
    options: {
      output: { type: 'string', short: 'o' },
      help: { type: 'boolean', short: 'h', default: false },
    },
    allowPositionals: false,
  });

  if (values.help) {
    console.log(`Usage: node scripts/generate-package-filters.mjs [--output <path>]

Options:
  --output, -o  Output file path (default: .github/generated/package-filters.yml)
  --help, -h    Show this help
`);
    process.exit(0);
  }

  return values;
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  const rootDir = getRepoRoot();
  const args = parseCliArgs(process.argv.slice(2));
  const outputFile = path.resolve(
    rootDir,
    args.output ?? '.github/generated/package-filters.yml',
  );

  generatePackageFilters({ rootDir, outputFile });
  console.log(`saved filters to ${path.relative(rootDir, outputFile)}`);
}
